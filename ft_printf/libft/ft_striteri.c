#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	ix;

	ix = 0;
	if (!s || !f)
		return ;
	while (*s)
	{
		f(ix, &*s);
		ix++;
		s++;
	}
}
